package org.broken.arrow.library.menu.button.logic;

import org.broken.arrow.library.logging.Validate;
import org.broken.arrow.library.menu.MenuUtility;
import org.bukkit.Material;
import org.bukkit.inventory.ItemStack;
import org.bukkit.inventory.meta.ItemMeta;
import org.checkerframework.checker.nullness.qual.NonNull;

import javax.annotation.Nonnull;
import java.util.Arrays;
import java.util.function.Consumer;

/**
 * Represents a dynamic, context-bound button template used to populate paginated or recurring layouts.
 * <p>
 * Unlike a standard button, a {@code FillMenuButton} is bound to a specific data type {@code <T>},
 * allowing individual slots to dynamically generate their visual items and click logic based on the
 * underlying data object mapped to that slot position.
 * </p>
 *
 * @param <T> the type of data object associated with this button template.
 */
public class FillMenuButton<T> {


    private FillClickAction<T> click;
    private ItemProvider<T> itemProvider;

    private boolean updateButtonsTimer;
    private long updateTime;

    /**
     * Constructs a new {@code FillMenuButton} with specified click and fill behaviors.
     *
     * @param click    the click handler defining actions when a filled slot is clicked; must not be {@code null}.
     * @param provider the function that generates the {@link org.bukkit.inventory.ItemStack} based on slot index and data object; must not be {@code null}.
     */
    public FillMenuButton(@Nonnull final FillClickAction<T> click, @Nonnull final ItemProvider<T> provider) {
        this.click = click;
        this.itemProvider = provider;
    }


    /**
     * Instantiates a new {@code FillMenuButton} using a localized configuration closure.
     *
     * @param <T>      the underlying data model type mapped to this menu component.
     * @param callback a configuration hook exposing a type-safe {@link FillButtonBuilder}.
     * @return a fully populated and immutable-ready {@code FillMenuButton} instance.
     */
    @Nonnull
    public static <T> FillMenuButton<T> make(@Nonnull final Consumer<FillButtonBuilder<T>> callback) {
        FillButtonBuilder<T> fillButtonBuilder = new FillButtonBuilder<>();
        callback.accept(fillButtonBuilder);
        return fillButtonBuilder.build();
    }

    /**
     * Creates a new {@code FillMenuButton} instance with both click and item retrieval logic.
     *
     * @param <T>         the data type bound to the button.
     * @param clickAction the handler containing the click logic.
     * @param provider    the logic determining how the display item is constructed.
     * @return a new {@code FillMenuButton} instance.
     */
    @Nonnull
    public static <T> FillMenuButton<T> make(@Nonnull final FillClickAction<T> clickAction, @Nonnull final ItemProvider<T> provider) {
        return new FillMenuButton<>(clickAction, provider);
    }

    /**
     * Checks if this button configuration is set to automatically update on an active timer cycle.
     *
     * @return {@code true} if auto-refreshing is enabled; {@code false} otherwise.
     */
    public boolean isUpdateButtonsTimer() {
        return updateButtonsTimer;
    }

    /**
     * Retrieves the custom tick update interval designated for this button configuration.
     *
     * @return the tick interval, or {@code -1} if falling back to the default menu update rate.
     */
    public long getUpdateTime() {
        return updateTime;
    }

    /**
     * Retrieves the click callback logic assigned to this button configuration.
     *
     * @return the active {@link FillClickAction} instance.
     */
    @Nonnull
    public FillClickAction<T> getClick() {
        return click;
    }

    /**
     * Retrieves the display item generation logic assigned to this button configuration.
     *
     * @return the active {@link ItemProvider} instance.
     */
    @Nonnull
    public ItemProvider<T> getItemProvider() {
        return itemProvider;
    }

    /**
     * Toggles whether the buttons generated by this template should dynamically refresh on an automated timer cycle.
     *
     * @param updateButtonsTimer {@code true} to enable automated background refreshes.
     */
    public void setUpdateButtonsTimer(boolean updateButtonsTimer) {
        this.updateButtonsTimer = updateButtonsTimer;
    }

    /**
     * Configures a specific tick interval loop for refreshing this button configuration.
     * <p>
     * Note: Automated refreshes will only occur if {@link #setUpdateButtonsTimer(boolean)} is enabled.
     * </p>
     *
     * @param updateTime the update loop delay in seconds, or {@code -1} to inherit the base menu's update rate from
     *                   this {@link MenuUtility#getUpdateTime()}.
     */
    public void setUpdateTime(long updateTime) {
        this.updateTime = updateTime;
    }


    /**
     * An isolated, fluent configuration surface used to construct a {@link FillMenuButton}.
     * <p>
     * This class acts exclusively as a setup pipeline within the {@link FillMenuButton#make(Consumer)}
     * framework. It defaults layout operations to safe, non-null structural fallback states to eliminate
     * accidental runtime exceptions if specific properties are omitted by the caller.
     * </p>
     *
     * @param <T> the data type bound to the button elements being processed.
     */
    public static final class FillButtonBuilder<T> {
        private FillClickAction<T> click = (player, inventory, clickType, stack, fillObject) -> ButtonUpdateAction.NONE;
        private ItemProvider<T> itemProvider = FillButtonBuilder::getFallBackStack;

        private boolean updateButtonsTimer = false;
        private long updateTime = -1;

        private FillButtonBuilder() {
        }

        /**
         * Assigns or overrides the structural behavior executed when a client interacts with this button slot.
         *
         * @param click the operational click callback logic.
         * @return this builder instance for fluent layout chaining.
         */
        public FillButtonBuilder<T> click(@Nonnull FillClickAction<T> click) {
            Validate.checkNotNull(click, "The click action can't be set to null.");
            this.click = click;
            return this;
        }

        /**
         * Assigns or overrides the layout strategy responsible for drawing visual items within the menu index.
         *
         * @param provider the item generation callback logic.
         * @return this builder instance for fluent layout chaining.
         */
        public FillButtonBuilder<T> itemProvider(@Nonnull ItemProvider<T> provider) {
            Validate.checkNotNull(provider, "The item provider can't be set to null.");
            this.itemProvider = provider;
            return this;
        }

        /**
         * Toggles whether the buttons generated by this template should dynamically refresh on an automated timer cycle.
         *
         * @param enable {@code true} to enable automated background refreshes.
         * @return this instance for builder chaining.
         */
        public FillButtonBuilder<T> useTimer(boolean enable) {
            this.updateButtonsTimer = enable;
            return this;
        }

        /**
         * Configures a specific tick interval loop for refreshing this button configuration.
         * <p>
         * Note: Automated refreshes will only occur if {@link #setUpdateButtonsTimer(boolean)} is enabled.
         * </p>
         *
         * @param seconds the update loop delay in seconds, or {@code -1} to inherit the base menu's update rate from
         *                this {@link MenuUtility#getUpdateTime()}.
         * @return this instance for builder chaining.
         */
        public FillButtonBuilder<T> updateInterval(long seconds) {
            this.updateTime = seconds;
            return this;
        }

        /**
         * Internal compiling engine that assembles and assigns properties to the finalized menu item.
         *
         * @return a cleanly initialized {@link FillMenuButton} instance.
         */
        @Nonnull
        private FillMenuButton<T> build() {
            FillMenuButton<T> button = new FillMenuButton<>(this.click, this.itemProvider);
            button.setUpdateButtonsTimer(this.updateButtonsTimer);
            button.setUpdateTime(this.updateTime);
            return button;
        }

        private static <T> @NonNull ItemStack getFallBackStack(int slot, T data) {
            ItemStack errorItem = new ItemStack(Material.BARRIER);
            ItemMeta meta = errorItem.getItemMeta();
            if (meta != null) {
                meta.setDisplayName("§c§lMissing ItemProvider");
                meta.setLore(Arrays.asList(
                        "§7This FillMenuButton is missing an item configuration.",
                        "§7Use §e.itemProvider(...) §7in your builder block.",
                        "§8Slot:§6 " + slot
                ));
                errorItem.setItemMeta(meta);
            }
            return errorItem;
        }
    }
}
